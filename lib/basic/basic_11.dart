void main() {
  int number = 2;

  print(number);
  print(number + 2);
  print(number - 2);
  print(number * 2);
  print(number / 2);

  print('----------');

  // 나머지
  print(number % 2);
  print(number % 3);

  print(number);

  print('----------');

  number++;

  print(number);

  number--;

  print(number);
}